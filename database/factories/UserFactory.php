$factory->define(App\User::class, function (Faker $faker) {
    static $password;
    $name = $faker->name;
    return [
        'name' => $name,
        'avatar' => 'public/defaults/avatars/male.png',
        'gender' => 0,
        'slug' => str_slug($name),
        'email' => $faker->unique()->safeEmail,
        'password' => $password ?: $password = bcrypt('secret'),
        'remember_token' => str_random(10),
    ];
});
